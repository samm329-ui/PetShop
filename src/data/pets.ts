export const getFeaturedPets = () => pets.filter(p => p.availability === 'Available').slice(0, 6);
export const getPetsByCategory = (category: string) => pets.filter(p => p.category === category);
export const getPetsByBreed = (breed: string) => pets.filter(p => p.breed === breed);
export const getPetById = (id: string) => pets.find(p => p.id === id);
export const getBreedsByCategory = (category: string) => {
  const cat = categories.find(c => c.slug === category);
  return cat ? cat.breeds : [];
};
